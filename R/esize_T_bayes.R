#' Bayesian Comparison of Tredoux Effective Size Across Two Lineups
#'
#' Fits independent Dirichlet-Multinomial models to two lineup tables and
#' returns the posterior distribution of the difference in Tredoux E'.
#'
#' @param table_a A table of choice counts for lineup A.
#' @param table_b A table of choice counts for lineup B.
#' @param alpha Dirichlet concentration parameter(s). Applied to both lineups.
#'   A scalar or a length-k vector (both lineups must have the same k).
#'   Default 0.5 (Jeffreys).
#' @param S Number of posterior draws per lineup. Default 10000.
#' @param credible_mass Width of the equal-tailed credible interval for the
#'   difference. Default 0.95.
#'
#' @details
#'   Independent Dirichlet-Multinomial posteriors are fitted for each lineup.
#'   The posterior of Delta = E'_A - E'_B is obtained by subtracting
#'   corresponding draws. P(E'_A > E'_B | data) is reported directly.
#'
#'   When the two lineups have different nominal sizes (different k), alpha
#'   must be a scalar; it is expanded independently for each lineup.
#'
#' @return An object of class \code{"esize_T_bayes_compare"} containing:
#'   \describe{
#'     \item{delta}{Posterior draws of Delta = E'_A - E'_B.}
#'     \item{posterior_mean}{Posterior mean of Delta.}
#'     \item{posterior_median}{Posterior median of Delta.}
#'     \item{credible_interval}{Named 2-vector (lower, upper) for Delta.}
#'     \item{P_A_greater}{Posterior probability that E'_A > E'_B.}
#'     \item{P_B_greater}{Posterior probability that E'_B > E'_A.}
#'     \item{result_a}{The \code{esize_T_bayes} object for lineup A.}
#'     \item{result_b}{The \code{esize_T_bayes} object for lineup B.}
#'     \item{credible_mass}{Credible interval width.}
#'     \item{S}{Number of posterior draws.}
#'   }
#'
#' @references
#'   Tredoux, C. G. (1998). Statistical inference on measures of lineup
#'   fairness. \emph{Law and Human Behavior, 22}(2), 217-237.
#'
#' @seealso \code{\link{esize_T_bayes}}, \code{\link{compare_eff_sizes.boot}}
#'
#' @examples
#' counts_a <- c(20, 5, 5, 5, 5, 5)
#' counts_b <- c(10, 8, 8, 8, 8, 8)
#' tab_a <- as.table(setNames(counts_a, 1:6))
#' tab_b <- as.table(setNames(counts_b, 1:6))
#'
#' result <- esize_T_bayes_compare(tab_a, tab_b)
#' print(result)
#' \dontrun{plot(result)}
#'
#' @importFrom stats quantile median
#' @export
esize_T_bayes_compare <- function(table_a,
                                  table_b,
                                  alpha         = 0.5,
                                  S             = 10000,
                                  credible_mass = 0.95) {

  ka <- length(as.numeric(table_a))
  kb <- length(as.numeric(table_b))

  if (length(alpha) > 1 && ka != kb) {
    stop(
      "alpha may only be a vector when both lineups have the same number of positions.",
      call. = FALSE
    )
  }

  res_a <- esize_T_bayes(table_a, alpha = alpha, S = S,
                         credible_mass = credible_mass)
  res_b <- esize_T_bayes(table_b, alpha = alpha, S = S,
                         credible_mass = credible_mass)

  delta <- res_a$E_draws - res_b$E_draws

  tail_prob <- (1 - credible_mass) / 2
  ci_delta  <- quantile(delta, probs = c(tail_prob, 1 - tail_prob))
  names(ci_delta) <- c("lower", "upper")

  structure(
    list(
      delta             = delta,
      posterior_mean    = mean(delta),
      posterior_median  = median(delta),
      credible_interval = ci_delta,
      P_A_greater       = mean(delta > 0),
      P_B_greater       = mean(delta < 0),
      result_a          = res_a,
      result_b          = res_b,
      credible_mass     = credible_mass,
      S                 = S
    ),
    class = "esize_T_bayes_compare"
  )
}

#' Print method for esize_T_bayes_compare objects
#'
#' @param x An object of class \code{"esize_T_bayes_compare"}.
#' @param ... Further arguments (unused).
#' @export
print.esize_T_bayes_compare <- function(x, ...) {
  cat("Bayesian Comparison: Tredoux Effective Size (E'_A vs E'_B)\n")
  cat("-----------------------------------------------------------\n")
  cat(sprintf("  Lineup A: k = %d, N = %d   |   Lineup B: k = %d, N = %d\n",
              x$result_a$k, x$result_a$n, x$result_b$k, x$result_b$n))
  cat(sprintf("  S (draws): %d    Prior: Dirichlet(alpha = %.2f)\n",
              x$S,
              if (length(unique(x$result_a$prior_alpha)) == 1)
                x$result_a$prior_alpha[1] else NA_real_))
  cat(sprintf("  E'_A posterior median: %.3f\n", x$result_a$posterior_median))
  cat(sprintf("  E'_B posterior median: %.3f\n", x$result_b$posterior_median))
  cat(sprintf("  Delta (E'_A - E'_B) posterior mean:   %.3f\n",
              x$posterior_mean))
  cat(sprintf("  Delta posterior median:               %.3f\n",
              x$posterior_median))
  cat(sprintf("  %.0f%% credible interval for Delta: [%.3f, %.3f]\n",
              x$credible_mass * 100,
              x$credible_interval["lower"],
              x$credible_interval["upper"]))
  cat(sprintf("  P(E'_A > E'_B | data): %.3f\n", x$P_A_greater))
  cat(sprintf("  P(E'_B > E'_A | data): %.3f\n", x$P_B_greater))
  invisible(x)
}

#' Plot method for esize_T_bayes_compare objects
#'
#' Plots the posterior distribution of Delta = E'_A - E'_B as a histogram
#' with density overlay. A vertical line at zero and shaded probability mass
#' above/below zero are shown.
#'
#' @param x An object of class \code{"esize_T_bayes_compare"}.
#' @param ... Further arguments (unused).
#' @return A ggplot object.
#' @export
plot.esize_T_bayes_compare <- function(x, ...) {
  df  <- data.frame(delta = x$delta)
  ci  <- x$credible_interval
  ttl <- sprintf(
    "Posterior of E'_A - E'_B\nP(E'_A > E'_B) = %.3f | %.0f%% CI: [%.2f, %.2f]",
    x$P_A_greater, x$credible_mass * 100, ci["lower"], ci["upper"]
  )
  ggplot2::ggplot(df, ggplot2::aes(x = delta)) +
    ggplot2::geom_histogram(
      ggplot2::aes(y = ggplot2::after_stat(density)),
      bins = 40, fill = "#72b7b2", alpha = 0.6
    ) +
    ggplot2::geom_density(color = "#1f2d3d", linewidth = 1) +
    ggplot2::geom_vline(xintercept = 0, linetype = "solid",
                        color = "#c0392b", linewidth = 0.8) +
    ggplot2::geom_vline(xintercept = ci["lower"], linetype = "dashed",
                        color = "#7f8c8d") +
    ggplot2::geom_vline(xintercept = ci["upper"], linetype = "dashed",
                        color = "#7f8c8d") +
    ggplot2::labs(
      x     = "Delta (E'_A - E'_B)",
      y     = "Density",
      title = ttl
    ) +
    ggplot2::theme_minimal(base_size = 12)
}
